def load_agents_from_file(filepath):
    try:
        with open(filepath, 'r') as f:
            agents = [line.strip() for line in f if line.strip()]
            if not agents:
                print(f"{warning()} Warning: File '{filepath}' kosong.")
                return None
            return agents
    except FileNotFoundError:
        print(f"{warning()} Error: File User-Agent tidak ditemukan di '{filepath}'")
        return None

def get_random_agent(custom_list=None):
    if custom_list:
        return random.choice(custom_list)
    else:
        return random.choice(USER_AGENTS)